package week1.day1;

public class LearnLooping {

	public static void main(String[] args) {
		//Print 1 to 5
		//for(initilisation; condition; increment/decrement)
		for(int i=1; i<=5; i++)
			System.out.println(i);
		for(int i=5; i>0; i--)
			System.out.println(i);
		//you can also print instead of numbers
		for(int i=1; i<=5; i++)
			System.out.println("Happy Birthday Dhanya");

	}

}
